<?php
function suma($a, $b){
    $resultado = $a + $b;
    return $resultado;
}

function resta($a, $b) {
    $resultado = $a - $b;
    return $resultado;
}

function multiplicacion($a, $b) {
    $resultado = $a * $b;
    return $resultado;
}

function division($a, $b) {
    $resultado = $a / $b;
    return $resultado;
}

?>